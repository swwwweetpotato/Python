# int

# int() : 다른 자료형의 값을 정수형으로 변환

print(int(1.9))   #소수점 제거(내림)
print(int(True))  # 1로 변환
print(int(False)) # 0으로 변환

# type() : 데이터타입 알려주는 함수

bin(1) # 2진수로 변환
oct(1) # 8진수로 변환
hex(1) # 16진수로 변환

# floating point number

print(float(True))  # 1.0변환
print(float(False)) # 0.0변환
print(float('3.14'))
